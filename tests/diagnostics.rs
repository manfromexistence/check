#[test]
fn blank_required_structured_messages_emit_invalid_runner_output() {
    let cases = [
        (
            "cargo-check",
            "cargo-json",
            DxToolTarget::Typecheck,
            br#"{"reason":"compiler-message","message":{"message":"","level":"error","spans":[]}}"#.as_slice(),
        ),
        (
            "ruff-check",
            "ruff-json",
            DxToolTarget::Lint,
            br#"[{"code":"F401","message":"","filename":"pkg/demo.py","location":{"row":2,"column":8}}]"#.as_slice(),
        ),
        (
            "biome-lint",
            "biome-json",
            DxToolTarget::Lint,
            br#"{"diagnostics":[{"severity":"error","message":"","category":"lint/suspicious/noDebugger"}],"command":"lint"}"#.as_slice(),
        ),
    ];

    for (id, parser, target, stdout) in cases {
        let plan = plan(id, parser, target);
        let diagnostics = parse_tool_output(&plan, stdout, b"");

        assert_eq!(diagnostics.len(), 1, "{id}");
        assert_eq!(diagnostics[0].id, format!("{id}:runner-output-invalid"));
        assert_eq!(diagnostics[0].severity, DxSeverity::Failure);
        assert!(
            diagnostics[0].message.contains("message"),
            "{id} should explain the missing message text: {:?}",
            diagnostics[0].message
        );
    }
}

