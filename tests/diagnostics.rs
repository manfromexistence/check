#[test]
fn web_audit_parser_reports_failure_diagnostics_hidden_by_display_cap() {
    let plan = plan("web-audit-home", "web-audit-json", DxToolTarget::Audit);
    let mut audit_diagnostics = (0..32)
        .map(|index| {
            json!({
                "id": format!("web-info-{index}"),
                "severity": "info",
                "message": format!("Informational web audit detail {index}"),
                "next_action": "Keep monitoring this signal."
            })
        })
        .collect::<Vec<_>>();
    audit_diagnostics.push(json!({
        "id": "web-hidden-failure",
        "severity": "failure",
        "message": "A failure appeared after the display cap.",
        "next_action": "Fix the hidden failure."
    }));
    let stdout = serde_json::to_vec(&json!({
        "schema_version": "dx.check.web_audit.v1",
        "url": "http://localhost:3000/",
        "diagnostics": audit_diagnostics
    }))
    .expect("web audit json");

    let diagnostics = parse_tool_output(&plan, &stdout, b"");

    assert!(
        diagnostics.iter().any(|diagnostic| {
            diagnostic.id == "web-audit-home:web-hidden-failure"
                && diagnostic.severity == DxSeverity::Failure
        }),
        "hidden failure must still block the score: {diagnostics:#?}"
    );
}

