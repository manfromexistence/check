fn plan(id: &str, parser: &str, target: DxToolTarget) -> DxToolPlan {
    DxToolPlan {
        id: id.to_string(),
        target,
        executable: "tool".to_string(),
        args: Vec::new(),
        cwd: PathBuf::from("G:\\demo"),
        detected_from: vec!["fixture".to_string()],
        parser: parser.to_string(),
    }
}

#[test]
fn every_planned_parser_has_explicit_test_coverage() {
    let root = tempdir().unwrap();
    fs::write(
        root.path().join("Cargo.toml"),
        "[package]\nname='demo'\nversion='0.1.0'\nedition='2024'\n",
    )
    .unwrap();
    fs::write(
        root.path().join("package.json"),
        r#"{
  "scripts": {
"lint": "eslint .",
"format:check": "prettier --check .",
"typecheck": "tsc --noEmit",
"test": "vitest run"
  },
  "devDependencies": {
"@biomejs/biome": "^2.4.16"
  }
}
"#,
    )
    .unwrap();
    fs::write(root.path().join("pyproject.toml"), "[tool.ruff]\n").unwrap();
    fs::write(root.path().join("go.mod"), "module demo\n").unwrap();
    fs::write(
        root.path().join("dx"),
        r#"
biome[target path enabled](
lint . true
format . true
)

web_audit_targets[id url required_status max_html_bytes](
home "http://localhost:3000/" 200 200000
)
"#,
    )
    .unwrap();
    fs::create_dir_all(root.path().join("src")).unwrap();
    fs::create_dir_all(root.path().join("build").join("debug")).unwrap();
    fs::write(
        root.path().join("src").join("main.cpp"),
        "int main() { return 0; }\n",
    )
    .unwrap();
    fs::write(root.path().join(".clang-format"), "BasedOnStyle: LLVM\n").unwrap();
    fs::write(root.path().join(".clang-tidy"), "Checks: bugprone-*\n").unwrap();
    fs::write(
        root.path().join("compile_commands.json"),
        r#"[
  { "directory": ".", "command": "clang++ -c src/main.cpp", "file": "src/main.cpp" }
]
"#,
    )
    .unwrap();
    fs::write(
        root.path().join("CMakeLists.txt"),
        "cmake_minimum_required(VERSION 3.25)\nproject(demo CXX)\n",
    )
    .unwrap();
    fs::write(
        root.path()
            .join("build")
            .join("debug")
            .join("CMakeCache.txt"),
        format!(
            "# configured CMake build directory\nCMAKE_HOME_DIRECTORY:INTERNAL={}\n",
            root.path().display()
        ),
    )
    .unwrap();
    fs::write(
        root.path()
            .join("build")
            .join("debug")
            .join("CTestTestfile.cmake"),
        format!(
            "# generated by CTest\n# Source directory: {}\n",
            root.path().display()
        ),
    )
    .unwrap();

    let planned = plan_tools(
        root.path(),
        &[
            DxToolTarget::Lint,
            DxToolTarget::Format,
            DxToolTarget::Typecheck,
            DxToolTarget::Test,
            DxToolTarget::Audit,
        ],
    )
    .into_iter()
    .map(|plan| plan.parser)
    .chain({
        let black_root = tempdir().unwrap();
        fs::write(
            black_root.path().join("pyproject.toml"),
            "[tool.black]\nline-length = 100\n",
        )
        .unwrap();
        plan_tools(black_root.path(), &[DxToolTarget::Format])
            .into_iter()
            .map(|plan| plan.parser)
            .collect::<Vec<_>>()
    })
    .collect::<BTreeSet<_>>();
    let covered = [
        "black",
        "biome-json",
        "cargo-json",
        "clang-format",
        "clangd",
        "clang-tidy",
        "cppcheck-xml",
        "ctest",
        "cxx-compiler",
        "gofmt-list",
        "go-test",
        "go-vet",
        "package-script",
        "pytest",
        "ruff-format",
        "ruff-json",
        "rustfmt",
        "web-audit-json",
    ]
    .into_iter()
    .map(ToOwned::to_owned)
    .collect::<BTreeSet<_>>();

    assert_eq!(planned, covered);
}

