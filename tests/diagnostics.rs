#[test]
fn parses_cargo_compiler_message_into_typed_diagnostic() {
    let output = r#"{"reason":"compiler-artifact","package_id":"demo 0.1.0 (path+file:///demo)"}"#
        .to_string()
        + "\n"
        + r#"{"reason":"compiler-message","message":{"message":"unused import: `std::fs`","code":{"code":"unused_imports","explanation":null},"level":"warning","spans":[{"file_name":"src\\lib.rs","line_start":3,"column_start":5,"line_end":3,"column_end":12,"is_primary":true}],"children":[],"rendered":"warning: unused import: `std::fs`"}}"#;

    let diagnostics = parse_cargo_json_lines("cargo-check", &output);

    assert_eq!(diagnostics.len(), 1);
    let diagnostic = &diagnostics[0];
    assert_eq!(diagnostic.id, "cargo-check:unused_imports");
    assert_eq!(diagnostic.source, "cargo-check");
    assert_eq!(diagnostic.severity, DxSeverity::Warning);
    assert_eq!(diagnostic.file.as_deref(), Some("src\\lib.rs"));
    assert_eq!(diagnostic.line, Some(3));
    assert_eq!(diagnostic.column, Some(5));
    assert_eq!(diagnostic.message, "unused import: `std::fs`");
    assert_eq!(diagnostic.measurement, DxMeasurementKind::Measured);
    assert!(diagnostic.next_action.contains("Fix the Rust diagnostic"));
}

