#[test]
fn default_rules_ignore_insecure_default_strings_inside_detection_code() {
    let root = tempdir().unwrap();
    fs::create_dir_all(root.path().join("src")).unwrap();
    fs::write(root.path().join("README.md"), "# Demo\n").unwrap();
    fs::write(
        root.path().join("src").join("lib.rs"),
        r#"
pub fn insecure_terms() -> [&'static str; 4] {
    ["password = \"", "secret = \"", "api_key = \"", "dangerouslySetInnerHTML"]
}
"#,
    )
    .unwrap();

    let report = analyze_project(root.path(), DxCheckEngineOptions::default()).unwrap();

    assert!(
        !report.findings.iter().any(|finding| {
            finding.id == "insecure-source-defaults"
                && finding.file.as_deref() == Some("src/lib.rs")
        }),
        "literal detection terms should not count as insecure defaults"
    );
}

#[test]
